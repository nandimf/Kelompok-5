    private void inputJumlahCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_inputJumlahCaretUpdate
        // TODO add your handling code here:
        if(!inputJumlah.getText().isEmpty()) {
            int harga = Integer.parseInt(inputHargaJual.getText());
            int jumlah = Integer.parseInt(inputJumlah.getText());
            String total = String.valueOf(harga*jumlah);
            inputTotalBayar.setText(total);
        }
    }//GEN-LAST:event_inputJumlahCaretUpdate

