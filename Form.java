    private void inputKodeBarangCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_inputKodeBarangCaretUpdate
        // TODO add your handling code here:
        try {
            CheckConnection();
            String kode = inputKodeBarang.getText();
            ResultSet result_data = getDataBarangByKode(kode);
            while(result_data.next()) {
                inputNmBarang.setText(result_data.getString("nama"));
                inputHargaBarang.setText(result_data.getString("harga"));
            }
            CloseConnection();
        } catch(SQLException e) {
            System.out.println("Error tampil data by kode: "+e.getMessage());
        }
    }//GEN-LAST:event_inputKodeBarangCaretUpdate

